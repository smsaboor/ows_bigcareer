      }
    }
  }
  void getSticker() {
    // Hide keyboard when sticker appear
    focusNode.unfocus();
    setState(() {
      isShowSticker = !isShowSticker;
    });
  }

  Future uploadFile() async {
    String fileName = DateTime.now().millisecondsSinceEpoch.toString();
    UploadTask uploadTask = chatProvider.uploadFile(imageFile!, fileName);
    try {
      TaskSnapshot snapshot = await uploadTask;
      imageUrl = await snapshot.ref.getDownloadURL();
      setState(() {
        isLoading = false;
        onSendMessage(imageUrl, TypeMessage.image);
      });
    } on FirebaseException catch (e) {
      setState(() {
        isLoading = false;
      });
      Fluttertoast.showToast(msg: e.message ?? e.toString());
    }
  }

  void onSendMessage(String content, int type) {
    print('onSendMessage: widget.isProjectChat${widget.isProjectChat}');
    if (content.trim().isNotEmpty) {
      textEditingController.clear();
      if (widget.isProjectChat) {
        chatProvider.sendMessage('messagesProjects', 'MyProjects', 'OthersProjects',content, type, groupChatId,
            currentUserId, widget.arguments.peerId, widget.isFirstTime);
      } else {
        chatProvider.sendMessage('messagesFreelancers', 'HireByMe', 'HireByOthers',content, type, groupChatId,
            currentUserId, widget.arguments.peerId, widget.isFirstTime);
      }
      if (listScrollController.hasClients) {
        listScrollController.animateTo(0,
            duration: const Duration(milliseconds: 300), curve: Curves.easeOut);
      }
    } else {
      Fluttertoast.showToast(
          msg: 'Nothing to send', backgroundColor: Colors.grey);
    }
  }

